/**
 * TWO TYPES OF LOOPS
 *
 * 1) WHILE
 * 2) FOR
 */

let x = 5;
while (x > 0) {
    console.log('hello');
    x = x - 1; // 5 -> 4 -> 3 -> 2 -> 1
}

for (let x = 5; x > 0; x = x - 1) {
    console.log('hello');
}

